/**\defgroup doc_CellMappings Cell Mappings
*
In order to for a cell to map an actual Finite Element we need an interface
class that can map a geometric cell to an element. The different types of
elements in existence is quite vast and here we only have a subset of the most
popular ones. The nice thing ChiTech is that we support elements beyond just
the traditional elements, i.e. triangles, quadrilaterals, tetrahedra and
hexahedra, to the general class of polygons and polyhedra.
*
* \ingroup doc_SpatialDiscretization*/
